package com.example.eventkeeper;

public class Fullname {
    private String first;
    private String last;


    public Fullname(String first, String last) {
        this.first = first;
        this.last = last;
    }

    public String getfirstName() {
        return first;
    }

    public String getlastName() {
        return last;
    }
}
